@SpringBootTest(classes = Application.class)
@AutoConfigureMockMvc
@RunWith(SpringRunner.class)
public class CommonGetControllerTest {

    @Autowired
    private MockMvc mockMvc;

    @MockBean(name = "userServiceImpl")
    private UserService userService;

    @Test
    public void indexRequestTestShouldReturnDefaultMessage() throws Exception {
        this.mockMvc.perform(get("/")).andDo(print()).andExpect(status().isOk())
                .andExpect(content().string(containsString("Online training application")));
    }

    @Test
    public void getUsersTestShouldReturnAllUsers() throws Exception {
        String uri = "/users";
        MvcResult mvcResult = mockMvc.perform(MockMvcRequestBuilders.get(uri)
                .accept(MediaType.APPLICATION_JSON_VALUE)).andReturn();

        int status = mvcResult.getResponse().getStatus();
        assertEquals(200, status);
        String content = mvcResult.getResponse().getContentAsString();
        User[] userlist = new ObjectMapper().readValue(content, User[].class);
        assertTrue(userlist.length > 0);
    }
}
